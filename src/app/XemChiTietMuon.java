    public void loadDataToCombobox() {
        cbbMM.addItem("Chọn");
        try {
            Statement st = connect.createStatement();
            ResultSet rs = st.executeQuery("select mamuon from muon;");
            while (rs.next()) {
                cbbMM.addItem(rs.getString(1));
            }
        } catch (SQLException e) {
            e.printStackTrace();
        }
    }

