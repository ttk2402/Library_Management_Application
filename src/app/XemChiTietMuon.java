    private void btnXemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXemActionPerformed
        // TODO add your handling code here:

        DefaultTableModel model = (DefaultTableModel) tbXCT.getModel();
        int rowCount = model.getRowCount();
        for (int i = rowCount - 1; i >= 0; i--) {
            model.removeRow(i);
        }
        String mamuon = (String) cbbMM.getSelectedItem();

        String sql = "select c.tensach from muon a, chitietmuon b, sach c where a.mamuon=b.mamuon and b.masach=c.masach and a.mamuon='" + mamuon + "';";
        try {
            Statement st = connect.createStatement();
            ResultSet rs = st.executeQuery(sql);
            while (rs.next()) {
                model.addRow(new Object[]{rs.getString(1)});
            }

        } catch (SQLException e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_btnXemActionPerformed

